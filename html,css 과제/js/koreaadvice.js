// 랜덤 명언을 뽑아주는 함수 : count만큼 명언을 뽑아서 배열로 리턴
const koreanAdviceGet = (count) => {
  let result = [];
  for (let i = 0; i < count; i++) {
    let randomIndex = Math.floor(Math.random() * koreaAdvice.length);
    result[i] = koreaAdvice[randomIndex];
    if (i !== 0) {
      // 두번째 넣은 값부터 중복되는 값이 있는지 확인
      const sameValueCheck = result.forEach((advice, index) => {
        if (advice.author === result[i].author) {
          return true
        } else {
          return false
        }
      })
      if (sameValueCheck) {
        // 중복이 있으면 다시 랜덤 인덱스 뽑기
        i--;
      }
    }
  }
  return result;
}

